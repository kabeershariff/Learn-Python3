# More Variables and Printing

name = "Kabeer"
age = 22
weight = 60
height = 175

# Format Strings/ F-Strings
# We put a f just before a string
# We can use this to embed variables inside strings we define the variable under {}

print(f"My name is {name} and my age is {age}")

total = age + weight + height
print(f"Adding {age}, {weight} and {height} we get {total}")
