#Logic in Python

print("Lets see NOT.")
print("not False = ", not False)
print("not True = ", not True, "\n")

print("Lets see OR Logic.")
print("True or False = ", True or False)
print("True or True = ", True or True)
print("False or True = ", False or True)
print("False or False = ", False or False, "\n")

print("Lets see AND Logic.")
print("True and False = ", True and False)
print("True and True = ", True and True)
print("False and True = ", False and True)
print("False and False = ", False and False, "\n")

print("Lets see NOT OR Logic.")
print("not(True or False) = ", not(True or False))
print("not(False or False) = ", not(False or False), "\n")

print("Lets see NOT AND Logic.")
print("not(True and False) = ", not(True and False))
print("not(True and True) = ", not(True and True), "\n")

print("Lets see (3 != 3) --> ", 3 != 3)
print("Lets see (3 == 3) --> ", 3 == 3)
